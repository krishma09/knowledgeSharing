<p>Let us go through various LAN technologies in brief:</p>
<h4>Ethernet</h4>
<p>Ethernet is a widely deployed LAN technology.This technology was invented by Bob Metcalfe and D.R. Boggs in the year 1970.  It was standardized in IEEE 802.3 in 1980.</p>
<p>Ethernet shares media.  Network which uses shared media has high probability of data collision.  Ethernet uses Carrier Sense Multi Access/Collision Detection (CSMA/CD) technology to detect collisions. On the occurrence of collision in Ethernet, all its hosts roll back, wait for some random amount of time, and then re-transmit the data.</p>
<p>Ethernet connector is,network interface card equipped with 48-bits MAC address.  This helps other Ethernet devices to identify and communicate with remote devices in Ethernet.</p>
<p>Traditional Ethernet uses 10BASE-T specifications.The number 10 depicts 10MBPS speed, BASE stands for baseband, and T stands for Thick Ethernet.  10BASE-T Ethernet provides transmission speed up to 10MBPS and uses coaxial cable or Cat-5 twisted pair cable with RJ-45 connector.  Ethernet follows star topology with segment length up to 100 meters.  All devices are connected to a hub/switch in a star fashion.  </p>
<h4>Fast-Ethernet</h4>
<p>To encompass need of fast emerging software and hardware technologies, Ethernet extends itself as Fast-Ethernet.  It can run on UTP, Optical Fiber, and wirelessly too.  It can provide speed up to 100 MBPS.  This standard is named as 100BASE-T in IEEE 803.2 using Cat-5 twisted pair cable.  It uses CSMA/CD technique for wired media sharing among the Ethernet hosts and CSMA/CA (CA stands for Collision Avoidance) technique for wireless Ethernet LAN. </p>
<p>Fast Ethernet on fiber is defined under 100BASE-FX standard which provides speed up to 100 MBPS on fiber.  Ethernet over fiber can be extended up to 100 meters in half-duplex mode and can reach maximum of 2000 meters in full-duplex over multimode fibers.</p>
<h4>Giga-Ethernet</h4>
<p>After being introduced in 1995, Fast-Ethernet could enjoy its high speed status only for 3 years till Giga-Ethernet introduced.  Giga-Ethernet provides speed up to 1000 mbits/seconds.  IEEE802.3ab standardize Giga-Ethernet over UTP using Cat-5, Cat-5e and Cat-6 cables.  IEEE802.3ah defines Giga-Ethernet over Fiber.</p>
<h4>Virtual LAN</h4>
<p>LAN uses Ethernet which in turn works on shared media.  Shared media in Ethernet create one single Broadcast domain and one single Collision domain.  Introduction of switches to Ethernet has removed single collision domain issue and each device connected to switch works in its separate collision domain.  But even Switches cannot divide a network into separate Broadcast domains.</p>
<p>Virtual LAN is a solution to divide a single Broadcast domain into multiple Broadcast domains.  Host in one VLAN cannot speak to a host in another.  By default, all hosts are placed into the same VLAN.</p>
<img src="images/VLAN.jpg" alt="Virtual LAN">
<p>In this diagram, different VLANs are depicted in different color codes.  Hosts in one VLAN, even if connected on the same Switch cannot see or speak to other hosts in different VLANs.  VLAN is Layer-2 technology which works closely on Ethernet.  To route packets between two different VLANs a Layer-3 device such as Router is required.</p>




