<p>A system of interconnected computers and computerized peripherals such as printers is called computer network.  This interconnection among computers facilitates information sharing among them.  Computers may connect to each other by either wired or wireless media.</p>
<h4>Classification of Computer Networks</h4>
<p>Computer networks are classified based on  various factors.They includes:</p>
<ul class="list">
<li>Geographical span</li>
<li>Inter-connectivity</li>
<li>Administration</li>
<li>Architecture</li>
</ul>
<h4>Geographical Span</h4>
<p>Geographically a network can be seen in one of the following categories:</p>
<ul class="list">
<li>It may be spanned across your table, among Bluetooth enabled devices,.  Ranging not more than few meters.</li>
<li>It may be spanned across a whole building, including intermediate devices to connect all floors.</li>
<li>It may be spanned across a whole city.</li>
<li>It may be spanned across multiple cities or provinces.</li>
<li>It may be one network covering whole world.</li>
</ul>
<h4>Inter-Connectivity</h4>
<p>Components of a network can be connected to each other differently in some fashion.  By connectedness we mean either logically , physically , or both ways.</p>
<ul class="list">
<li>Every single device can be connected to every other device on network, making the network mesh.</li>
<li>All devices can be connected to a single medium but geographically disconnected, created bus like structure.</li>
<li>Each device is connected to its left and right peers only, creating linear structure.</li>
<li>All devices connected together with a single device, creating star like structure.</li>
<li>All devices connected arbitrarily using all previous ways to connect each other, resulting in a hybrid structure.</li>
</ul>
<h4>Administration</h4>
<p>From an administratorâ€™s point of view, a network can be private network which belongs a single autonomous system and cannot be accessed outside its physical or logical domain.A network can be public which is accessed by all.</p>
<h4>Network Architecture</h4>
<ul class="list">
<p>Computer networks can be discriminated into various types such as Client-Server,peer-to-peer or hybrid, depending upon its architecture.</p>
<li>There can be one or more systems acting as Server.  Other being Client, requests the Server to serve requests.Server takes and processes request on behalf of Clients.</li>
<li>Two systems can be connected Point-to-Point, or in back-to-back fashion.  They both reside at the same level and called peers.</li>
<li>There can be hybrid network which involves network architecture of both the above types.</li>
</ul>
<h4>Network Applications</h4>
<p>Computer systems and peripherals are connected to form a network.They provide numerous advantages:</p>
<ul class="list">
<li>Resource sharing such as printers and storage devices</li>
<li>Exchange of information by means of e-Mails and FTP</li>
<li>Information sharing by using Web or Internet</li>
<li>Interaction with other users using dynamic web pages</li>
<li>IP phones</li>
<li>Video conferences</li>
<li>Parallel computing</li>
<li>Instant messaging</li>
</ul>





