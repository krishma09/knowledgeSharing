<td><p>It represents ordered collection of an object that can be <b>indexed</b> individually.</p>
<p>It is basically an alternative to an array. However, unlike array, you can add and remove items from a list at a specified position using an <b>index</b> and the array resizes itself automatically. It also allows dynamic memory allocation, add, search and sort items in the list.</p></td>
</tr>
<tr>
<td><p>Hashtable</a></p></td>
<td><p>It uses a <b>key</b> to access the elements in the collection.</p>
<p>A hash table is used when you need to access elements by using key, and you can identify a useful key value. Each item in the hash table has a <b>key/value</b> pair. The key is used to access the items in the collection.</p></td>
</tr>
<tr>
<td><p>SortedList</a></p></td>
<td><p>It uses a <b>key</b> as well as an <b>index</b> to access the items in a list.</p>
<p>A sorted list is a combination of an array and a hash table. It contains a list of items that can be accessed using a key or an index. If you access items using an index, it is an ArrayList, and if you access items using a key, it is a Hashtable. The collection of items is always sorted by the key value.</p></td>
</tr>
