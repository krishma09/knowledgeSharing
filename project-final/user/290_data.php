<p>Collection classes are specialized classes for data storage and retrieval. These classes provide support for stacks, queues, lists, and hash tables. Most collection classes implement the same interfaces. </p>
<p>Collection classes serve various purposes, such as allocating memory dynamically to elements and accessing a list of items on the basis of an index, etc. These classes create collections of objects of the Object class, which is the base class for all data types in VB.Net.</p>
<h5>Various Collection Classes and Their Usage</h5>
<p>The following are the various commonly used classes of the <b>System.Collection</b> namespace. Click the following links to check their details.</p>
