<p>In this chapter, we will study about <b>General Settings</b> in WordPress. WordPress general setting is used to set the basic configuration settings for your site. In the setting administration screen, it is a default setting screen.</p>
<p>Following are the steps to access the general settings −</p>
<p><b>Step 1</b> − Click on <b>Settings → General</b> option in WordPress.</p>
<img src="images/wordpress-general-setting-step1.jpg" alt="WordPress General Setting">
<p><b>Step 2</b> − The General Setting page is displayed as shown in the following snapshot.</p>
<img src="images/wordpress-general-setting-step2.jpg" alt="WordPress General Setting">
<p>Following are the details of the fields on general settings page.</p>
<ul class="list">
<li><p><b>Site Title</b> − It displays the name of the site in the template header.</p></li>
<li><p><b>Tagline</b> − Displays a short sentence about your site.</p></li>
<li><p><b>WordPress Address (URL)</b> − It is the URL of WordPress directory where your all core application files are present.</p></li>
<li><p><b>Site Address(URL)</b> − Enter the site URL which you want your site to display on the browser.</p></li>
<li><p><b>E-mail Address</b> −  Enter your e-mail address which helps to recover your password or any update.</p></li>
<li><p><b>Membership</b> − Anyone can register an account on your site after you check this checkbox.</p></li>
<li><p><b>New User Default Role</b> − The default role is set for the newly registered user or members.</p></li>
<li><p><b>Timezone</b> − Sets the time zone based on the particular city.</p></li>
<li><p><b>Date Format</b> − Sets the date format as you need to display on the site.</p></li>
<li><p><b>Time Format</b> − Sets the time format as you need to display on the site.</p></li>
<li><p><b>Week Starts On</b> − Select the week day which you prefer to start for WordPress calendar. By default it is set as Monday.</p></li>
<li><p><b>Site Language</b> − Sets the language for the WordPress dashboard.</p></li>
</ul>
<p><b>Step3</b> − After filling all the information about general settings, click on <b>Save Changes</b> button. It saves all your general setting information.</p>
