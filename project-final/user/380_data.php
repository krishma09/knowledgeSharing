<p>Switching is process to forward packets coming in from one port to a port leading towards the destination.  When data comes on a port it is called ingress, and when data leaves a port or goes out it is called egress.  A communication system may include number of switches and nodes.  At broad level, switching can be divided into two major categories:</p>
<ul class="list">
<li><p><b>Connectionless:</b>&nbsp;The data is forwarded on behalf of forwarding tables.  No previous handshaking is required and acknowledgements are optional.</p></li>
<li><p><b>Connection Oriented:</b>&nbsp;  Before switching data to be forwarded to destination, there is a need to pre-establish circuit along the path between both endpoints.  Data is then forwarded on that circuit.  After the transfer is completed, circuits can be kept for future use or can be turned down immediately.</p></li>
</ul>
<h4>Circuit Switching</h4>
<p>When two nodes communicate with each other over a dedicated communication path, it is called circuit switching.There 'is a need of pre-specified route from which data will travels and no other data is permitted.In circuit switching, to transfer the data, circuit must be established so that the data transfer can take place.</p>
<p>Circuits can be permanent or temporary.  Applications which use circuit switching may have to go through three phases:</p>
<ul class="list">
<li><p>Establish a circuit</p></li>
<li><p>Transfer the data</p></li>
<li><p>Disconnect the circuit</p></li>
</ul>
<img src="images/circuit_switching.jpg" alt="Circuit Switching">
<p>Circuit switching was designed for voice applications.  Telephone is the best suitable example of circuit switching.  Before a user can make a call, a virtual path between caller and callee is established over the network.</p>
<h4>Message Switching</h4>
<p>This technique was somewhere in middle of circuit switching and packet switching.  In message switching, the whole message is treated as a data unit and is switching / transferred in its entirety.</p>
<p>A switch working on message switching, first receives the whole message and buffers it until there are resources available to transfer it to the next hop.  If the next hop is not having enough resource to accommodate large size message, the message is stored and switch waits.</p>
<img src="images/message_switching.jpg" alt="Message Switching">
<p>This technique was considered substitute to circuit switching.  As in circuit switching the whole path is blocked for two entities only.  Message switching is replaced by packet switching.  Message switching has the following drawbacks:</p>
<ul class="list">
<li><p>Every switch in transit path needs enough storage to accommodate entire message.</p></li>
<li><p>Because of store-and-forward technique and waits included until resources are available, message switching is very slow.</p></li>
<li><p>Message switching was not a solution for streaming media and real-time applications.</p></li>
</ul>
<h4>Packet Switching</h4>
<p>Shortcomings of message switching gave birth to an idea of packet switching.  The entire message is broken down into smaller chunks called packets.  The switching information is added in the header of each packet and transmitted independently.</p>
<p>It is easier for intermediate networking devices to store small size packets and they do not take much resources either on carrier path or in the internal memory of switches.</p>
<img src="images/packet_switching.jpg" alt="Packet Switching">
<p>Packet switching enhances line efficiency as packets from multiple applications can be multiplexed over the carrier.  The internet uses packet switching technique.  Packet switching enables the user to differentiate data streams based on priorities.  Packets are stored and forwarded according to their priority to provide quality of service.</p>





