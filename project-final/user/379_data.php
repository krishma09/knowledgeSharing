<p>Multiplexing is a technique by which different analog and digital streams of transmission can be simultaneously processed over a shared link.  Multiplexing divides the high capacity medium into low capacity logical medium which is then shared by different streams.</p>
<p>Communication is possible over the air (radio frequency), using a physical media (cable), and light (optical fiber).  All mediums are capable of multiplexing.</p>
<p>When multiple senders try to send over a single medium, a device called Multiplexer divides the physical channel and allocates one to each.  On the other end of communication, a De-multiplexer receives data from a single medium, identifies each, and sends to different receivers.</p>
<h4>Frequency Division Multiplexing</h4>
<p>When the carrier is frequency, FDM is used.  FDM is an analog technology.  FDM divides the spectrum or carrier bandwidth in logical channels and allocates one user to each channel.  Each user can use the channel frequency independently and has exclusive access of it.  All channels are divided in such a way that they do not overlap with each other.  Channels are separated by guard bands.  Guard band is a frequency which is not used by either channel.</p>
<img src="images/fdm.jpg" alt="Frequency Division Multiplexing">
<h4>Time Division Multiplexing</h4>
<p>TDM is applied primarily on digital signals but can be applied on analog signals as well.  In TDM the shared channel is divided among its user by means of time slot.  Each user can transmit data within the provided time slot only.  Digital signals are divided in frames, equivalent to time slot i.e. frame of an optimal size which can be transmitted in given time slot.</p>
<p>TDM works in synchronized mode.  Both ends, i.e. Multiplexer and De-multiplexer are timely synchronized and both switch to next channel simultaneously.</p>

<img src="images/tdm.jpg" alt="Time Division Multiplexing">
<p>When channel A transmits its frame at one end,the De-multiplexer provides media to channel A on the other end.As soon as the channel Aâ€™s time slot expires, this side switches to channel B.  On the other end, the De-multiplexer works in a synchronized manner and provides media to channel B.  Signals from different channels travel the path in interleaved manner.</p>
<h4>Wavelength Division Multiplexing</h4>
<p>Light has different wavelength (colors).  In fiber optic mode, multiple optical carrier signals are multiplexed into an optical fiber by using different wavelengths.  This is an analog multiplexing technique and is done conceptually in the same manner as FDM but uses light as signals.</p>
<img src="images/wdm.jpg" alt="Wavelength Division Multiplexing">
<p>Further, on each wavelength time division multiplexing can be incorporated to accommodate more data signals.</p>
<h4>Code Division Multiplexing</h4>
<p>Multiple data signals can be transmitted over a single frequency by using Code Division Multiplexing.  FDM divides the frequency in smaller channels but CDM allows its users to full bandwidth and transmit signals all the time using a unique code.  CDM uses orthogonal codes to spread signals.</p>
<p>Each station is assigned with a unique code, called chip.  Signals travel with these codes independently, inside the whole bandwidth.The receiver knows in advance the chip code signal it has to receive.</p>





