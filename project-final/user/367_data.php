<p>All the Unix commands come with a number of optional and mandatory options. It is very common to forget the complete syntax of these commands.</p>
<p>Because no one can possibly remember every Unix command and all its options, we have online help available to mitigate this right from when Unix was at its development stage.</p>
<p>Unix's version of <b>Help files</b> are called <b>man pages</b>. If there is a command name and you are not sure how to use it, then Man Pages help you out with every step.</p>
<h5>Syntax</h5>
<p>Here is the simple command that helps you get the detail of any Unix command while working with the system −</p>
<pre class="result notranslate">$man command
</pre>
<h5>Example</h5>
<p>Suppose there is a command that requires you to get help; assume that you want to know about <b>pwd</b> then you simply need to use the following command −</p>
<pre class="result notranslate">$man pwd
</pre>
<p>The above command helps you with the complete information about the <b>pwd</b> command. Try it yourself at your command prompt to get more detail.</p>
<p>You can get complete detail on <b>man</b> command itself using the following command −</p>
<pre class="result notranslate">$man man
</pre>
