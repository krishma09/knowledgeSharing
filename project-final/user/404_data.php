<p><b>Step(2)</b> − The Reading Settings page is displayed as shown in the following screen.</p>
<img src="images/wordpress-reading-settings-step2.jpg" alt="WordPress Reading Setting">
<p>Following are the details of the fields on reading settings.</p>
<ul class="list">
<li><p><b>Front page displays</b> − This section is used to display the front page in any of the following format −</p>
<ul class="list">
<li><p><b>Your latest posts</b> − It displays latest posts on the front page.</p></li>
<li><p><b>A static page</b> − It displays the static pages on the front page.</p></li>
<li><p><b>Front Page</b> − You can select the actual page you want to display on front page from the drop down.</p></li>
<li><p><b>Posts Page</b> − You can select the page from the drop down which contains posts.</p></li>
</ul></li>
<li><p><b>Blog pages show at most</b> − The number of posts to be displayed per page or site. By default, it is set as 10.</p></li>
<li><p><b>Syndication feeds show the most recent</b> − The user can view the number of posts when they download one of the site feeds. By default, it is set as 10.</p></li>
<li><p><b>For each article in a feed, show</b> − This section is used to display the post by selecting any of the following formats −</p>
<ul class="list">
