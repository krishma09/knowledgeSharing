<p>Networking engineering is a complicated task, which involves software, firmware, chip level engineering, hardware, and electric pulses.  To ease network engineering, the whole networking concept is divided into multiple layers.  Each layer is involved in some particular task and is independent of all other layers.  But as a whole, almost all networking tasks depend on all of these layers.  Layers share data between them and they depend on each other only to take input and send output.</p>
<h4>Layered Tasks</h4>
<p>In layered architecture of Network Model, one whole network process is divided into small tasks.  Each small task is then assigned to a particular layer which works dedicatedly to process the task only.  Every layer does only specific work.</p>
<p>In layered communication system, one layer of a host deals with the task done by or to be done by its peer layer at the same level on the remote host.  The task is either initiated by layer at the lowest level or at the top most level.  If the task is initiated by the-top most layer, it is passed on to the layer below it for further processing.  The lower layer does the same thing, it processes the task and passes on to lower layer.  If the task is initiated by lower most layer, then the reverse path is taken.</p>
<img src="images/layered_tasks.jpg" alt="Layered Tasks">
<p>Every layer clubs together all procedures, protocols, and methods which it requires to execute its piece of task.  All layers identify their counterparts by means of encapsulation header and tail.</p>
<h4>OSI Model</h4>
<p>Open System Interconnect is an open standard for all communication systems.  OSI model is established by International Standard Organization (ISO).  This model has seven layers:</p>
<img src="images/OSI_model.jpg" alt="OSI Model">
<ul class="list">
<li><p><b>Application Layer</b>: This layer is responsible for providing interface to the application user.  This layer encompasses protocols which directly interact with the user.</p></li>
<li><p><b>Presentation Layer</b>: This layer defines how data in the native format of remote host should be presented in the native format of host.</p></li>
<li><p><b>Session Layer</b>: This layer maintains sessions between remote hosts.  For example, once user/password authentication is done, the remote host maintains this session for a while and does not ask for authentication again in that time span.</p></li>
<li><p><b>Transport Layer</b>:  This layer is responsible for end-to-end delivery between hosts.</p></li>
<li><p><b>Network Layer</b>: This layer is responsible for address assignment and uniquely addressing hosts in a network.</p></li>
<li><p><b>Data Link Layer</b>: This layer is responsible for reading and writing data from and onto the line.  Link errors are detected at this layer.</p></li>
<li><p><b>Physical Layer</b>: This layer defines the hardware, cabling wiring, power output, pulse rate etc.</p></li>
</ul>
<h4>Internet Model</h4>
<p>Internet uses TCP/IP protocol suite, also known as Internet suite.  This defines Internet Model which contains four layered architecture.  OSI Model is general communication model but Internet Model is what the internet uses for all its communication.The internet is independent of its underlying network architecture so is its Model.  This model has the following layers:</p>

<ul class="list">
<li><p><b>Application Layer</b>: This layer defines the protocol which enables user to interact with the network.For example, FTP, HTTP etc.</p></li>
<li><p><b>Transport Layer</b>: This layer defines how data should flow between hosts.  Major protocol at this layer is Transmission Control Protocol (TCP). This layer ensures data delivered between hosts is in-order and is responsible for end-to-end delivery.</p></li>
<li><p><b>Internet Layer</b>: Internet Protocol (IP) works on this layer.  This layer facilitates host addressing and recognition.  This layer defines routing.</p></li>
<li><p><b>Link Layer</b>: This layer provides mechanism of sending and receiving actual data.Unlike its OSI Model counterpart, this layer is independent of underlying network architecture and hardware.</p></li>
</ul>





