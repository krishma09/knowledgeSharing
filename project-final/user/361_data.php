<p>You will use different loops based on the situation. For example, the <b>while</b> loop executes the given commands until the given condition remains true; the <b>until</b> loop executes until a given condition becomes true.</p>
<p>Once you have good programming practice you will gain the expertise and thereby, start using appropriate loop based on the situation. Here, <b>while</b> and <b>for</b> loops are available in most of the other programming languages like <b>C</b>, <b>C++</b> and <b>PERL</b>, etc.</p>
<h4>Nesting Loops</h4>
<p>All the loops support nesting concept which means you can put one loop inside another similar one or different loops. This nesting can go up to unlimited number of times based on your requirement.</p>
<p>Here is an example of nesting <b>while</b> loop. The other loops can be nested based on the programming requirement in a similar way −</p>
<h4>Nesting while Loops</h4>
<p>It is possible to use a while loop as part of the body of another while loop.</p>
<h5>Syntax</h5>
<pre class="result notranslate">while command1 ; # this is loop1, the outer loop
do
   Statement(s) to be executed if command1 is true

   while command2 ; # this is loop2, the inner loop
   do
      Statement(s) to be executed if command2 is true
   done

   Statement(s) to be executed if command1 is true
done
</pre>
