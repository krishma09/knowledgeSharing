<p>In this chapter, we will discuss shell loops in Unix. A loop is a powerful programming tool that enables you to execute a set of commands repeatedly. In this chapter, we will examine the following types of loops available to shell programmers −</p>
<ul class="list">
<li>The while loop</a></li>
<li>The for loop</a></li>
<li>The until loop</a></li>
<li>The select loop</a></li>
