<p>There are many built-in dialog boxes to be used in Windows forms for various tasks like opening and saving files, printing a page, providing choices for colors, fonts, page setup, etc., to the user of an application. These built-in dialog boxes reduce the developer's time and workload. </p>
<p>All of these dialog box control classes inherit from the <b>CommonDialog</b> class and override the <i>RunDialog()</i> function of the base class to create the specific dialog box.</p>
<p>The RunDialog() function is automatically invoked when a user of a dialog box calls its <i>ShowDialog()</i> function. </p>
<p>The <b>ShowDialog</b> method is used to display all the dialog box controls at run-time. It returns a value of the type of <b>DialogResult</b> enumeration. The values of DialogResult enumeration are:</p>
<ul class="list">
<li><p><b>Abort</b> - returns DialogResult.Abort value, when user clicks an Abort button. </p></li>
<li><p><b>Cancel</b>- returns DialogResult.Cancel, when user clicks a Cancel button. </p></li>
<li><p><b>Ignore</b> - returns DialogResult.Ignore, when user clicks an Ignore button.</p></li>
<li><p><b>No</b> - returns DialogResult.No, when user clicks a No button.</p></li>
<li><p><b>None</b> - returns nothing and the dialog box continues running. </p></li>
<li><p><b>OK</b> - returns DialogResult.OK, when user clicks an OK button </p></li>
<li><p><b>Retry</b> - returns DialogResult.Retry , when user clicks an Retry button </p></li>
<li><p><b>Yes</b> - returns DialogResult.Yes, when user clicks an Yes button </p></li>
</ul>
<p>The following diagram shows the common dialog class inheritance:</p>
<img src="images/vb.net_dialog_boxes1.jpg" alt="VB.Net Dialog Boxes">
<p>All these above-mentioned classes have corresponding controls that could be added from the Toolbox during design time. You can include relevant functionality of these classes to your application, either by instantiating the class programmatically or by using relevant controls. </p>
<p>When you double click any of the dialog controls in the toolbox or drag the control onto the form, it appears in the Component tray at the bottom of the Windows Forms Designer, they do not directly show up on the form. </p>
